import java.util.*;
class Loops{
    public static void main(String[] args){
        
        // for loop 

        // for(initilisaion; Condition; increment/decrement){

        // }


        System.out.println("For loop : ");
        for(int i=1; i<=5;i++){
            System.out.println(i);
        }    




        System.out.println("While loop : ");
        int i=1;
        while(i<=5){
            System.out.println(i);
            i++;
        }


        // do while loop 


        
    }
}