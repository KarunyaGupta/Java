import java.util.*;
class PrettyPrinting{
    public static void main(String[] args){
        // till two digit decimal number
        float a = 435.2371341f;
        System.out.printf("Formatted number is %.2f",a); // roundoff

        System.out.printf("\nPie : %.3f",Math.PI);

        System.out.printf("\nHello my name is %s and I am %s" , "Karunya","Cool");
        
        System.out.printf("\n%d",10);

    }
}